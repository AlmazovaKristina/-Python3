x = int(input('Введите координату X:'))
y = int(input('Введите координату Y:'))
if x == 0 and y == 0:
    print('Точка не лежит на плоскости')
elif x > 0 and y > 0:
    print('Точка находиться в 1-ой плоскости ')
elif x < 0 and y > 0:
    print('Точка находиться во 2-ой плоскости ')
elif x < 0 and y < 0:
    print('Точка находиться в 3-ей плоскости ')
elif x > 0 and y < 0:
    print('Точка находиться в 4-ой плоскости ')
